--카트 테이블
CREATE TABLE CART (
    CART_ID      NUMBER PRIMARY KEY,
    MEM_ID       VARCHAR2(50) NOT NULL,
    PRO_ID       NUMBER NOT NULL,
    OPTION_ID    NUMBER NOT NULL,
    CART_STOCK   NUMBER DEFAULT 1 NOT NULL,
    CREATE_DATE  DATE DEFAULT SYSDATE
);

--오더 기본 테이블
CREATE TABLE ORDERS (
    ORDER_ID        NUMBER PRIMARY KEY,
    MEM_ID          VARCHAR2(50) NOT NULL,
    RECEIVER_NAME   VARCHAR2(50) NOT NULL,
    RECEIVER_PHONE  VARCHAR2(30) NOT NULL,
    RECEIVER_ADDR   VARCHAR2(300) NOT NULL,
    DELIVERY_MEMO   VARCHAR2(500),
    PAYMENT         VARCHAR2(30) NOT NULL,
    TOTAL_PRICE     NUMBER NOT NULL,
    ORDER_DATE      DATE DEFAULT SYSDATE,
    ORDER_STATUS    VARCHAR2(30) DEFAULT '주문완료'
);

--오더 상세 테이블
CREATE TABLE ORDER_DETAIL (
    DETAIL_ID    NUMBER PRIMARY KEY,
    ORDER_ID     NUMBER NOT NULL,
    PRO_ID       NUMBER NOT NULL,
    PRO_OP_ID    NUMBER,
    PRO_NAME     VARCHAR2(100) NOT NULL,
    PRO_COLOR    VARCHAR2(50),
    PRO_SIZE     VARCHAR2(50),
    QUANTITY     NUMBER NOT NULL,
    PRO_PRICE    NUMBER NOT NULL,
    SUM_PRICE    NUMBER NOT NULL,
    
    CONSTRAINT FK_ORDER_DETAIL_ORDER
    FOREIGN KEY (ORDER_ID)
    REFERENCES ORDERS (ORDER_ID)
);

-- 오더 기본 시퀀스
CREATE SEQUENCE ORDERS_SEQ
START WITH 1
INCREMENT BY 1
NOCACHE;

