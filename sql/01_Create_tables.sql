 --상품 기본테이블 생성
CREATE TABLE PRODUCTS(
PRO_ID NUMBER PRIMARY KEY,
PRO_NAME VARCHAR2(50) NOT NULL,
PRO_PRICE NUMBER(10) NOT NULL,
PRO_CONTENT VARCHAR2(1000) NOT NULL,
PRO_IMG VARCHAR2(200),
PRO_CATEGORY VARCHAR2(20) NOT NULL,
PRO_DATE DATE DEFAULT SYSDATE NOT NULL
);


--상품id 시퀀스 생성

CREATE SEQUENCE SEQ_PRODUCTS
START WITH 000001
INCREMENT BY 1
NOCACHE
NOCYCLE;


--상품옵션 테이블 생성

CREATE TABLE PRO_OPTION(
OPTION_ID NUMBER PRIMARY KEY,
PRO_ID NUMBER NOT NULL,
PRO_SIZE VARCHAR2(10) DEFAULT 'FREE' NOT NULL,
PRO_COLOR VARCHAR2(30) NOT NULL,
PRO_STOCK NUMBER(4) DEFAULT 0 NOT NULL,

CONSTRAINT FK_PRO_OPTION
FOREIGN KEY (PRO_ID)
REFERENCES PRODUCTS(PRO_ID),

CONSTRAINT UQ_PRO_OPTION
 UNIQUE (PRO_ID, PRO_SIZE, PRO_COLOR),

CONSTRAINT CK_PRO_OPTION_STOCK
CHECK (PRO_STOCK>=0)

);


